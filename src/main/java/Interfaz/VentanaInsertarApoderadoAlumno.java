    private void jbPrincipalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPrincipalActionPerformed
        // TODO add your handling code here:
        jLabelError.setText("-");

        alumnoApoderado alumApo = new alumnoApoderado();
        AlumnoApoderadoBO alumnoApoderadoBO = new AlumnoApoderadoBO();

        if (jTextParentesco.getText().isEmpty()) {
            jLabelError.setText("El campo parentesco no debe estar vac√≠a");
            return;
        }

        alumApo.setParentesco(jTextParentesco.getText());
        alumApo.setAlumno_id(alumno_id);
        alumApo.setApoderado_id(Integer.parseInt(jTextApoderadoId.getText()));

        try {
            alumnoApoderadoBO.insertar(alumApo);
            
            dispose();
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_jbPrincipalActionPerformed

